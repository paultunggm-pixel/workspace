import Foundation

/// A chat message within a session
struct ChatMessage: Identifiable, Codable, Equatable {
    var id: UUID
    var role: MessageRole
    var content: String
    var timestamp: Date
    var attachments: [ChatAttachment]
    var artifactFiles: [String]   // filenames generated by Claude
    var isStreaming: Bool

    init(
        id: UUID = UUID(),
        role: MessageRole,
        content: String,
        timestamp: Date = Date(),
        attachments: [ChatAttachment] = [],
        artifactFiles: [String] = [],
        isStreaming: Bool = false
    ) {
        self.id = id
        self.role = role
        self.content = content
        self.timestamp = timestamp
        self.attachments = attachments
        self.artifactFiles = artifactFiles
        self.isStreaming = isStreaming
    }

    enum MessageRole: String, Codable {
        case user = "用户"
        case assistant = "Claude"
        case system = "系统"
    }
}

/// File attachment in a message
struct ChatAttachment: Identifiable, Codable, Equatable {
    var id: UUID
    var fileName: String
    var filePath: String
    var fileSize: Int64

    init(id: UUID = UUID(), fileName: String, filePath: String, fileSize: Int64 = 0) {
        self.id = id
        self.fileName = fileName
        self.filePath = filePath
        self.fileSize = fileSize
    }
}

/// A persistent chat session
struct ChatSession: Identifiable, Codable, Equatable {
    var id: UUID
    var title: String
    var projectId: UUID
    var messages: [ChatMessage]
    var isActive: Bool
    var createdAt: Date
    var updatedAt: Date
    var totalTokens: Int

    init(
        id: UUID = UUID(),
        title: String = "新会话",
        projectId: UUID,
        messages: [ChatMessage] = [],
        isActive: Bool = true,
        createdAt: Date = Date(),
        updatedAt: Date = Date(),
        totalTokens: Int = 0
    ) {
        self.id = id
        self.title = title
        self.projectId = projectId
        self.messages = messages
        self.isActive = isActive
        self.createdAt = createdAt
        self.updatedAt = updatedAt
        self.totalTokens = totalTokens
    }

    var lastMessage: ChatMessage? {
        messages.last
    }

    var preview: String {
        lastMessage?.content.prefix(80).replacingOccurrences(of: "\n", with: " ")
            ?? "新会话"
    }
}
